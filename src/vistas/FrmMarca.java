    private void cargarTbl()
    {
        DefaultTableModel tbl = new DefaultTableModel();
        Object[] fila = new Object[3];
        tbl.addColumn("Nro");
        tbl.addColumn("Nombre");
        tbl.addColumn("Estado");
        int nro = 1;
        for(Marca m : this.cp.getMarcas())
        {
            fila[0] = nro;
            fila[1] = m.getNombre();
            if(m.getEstado())
            {
                fila[2]="En uso";
            }else{
                fila[2]="no disponible";
            }
            nro++;
            tbl.addRow(fila);
        }
        this.tblMarcas.setModel(tbl);
    }

